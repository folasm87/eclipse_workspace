package com.multivision.ehrms.action.form;

import org.apache.struts.validator.ValidatorForm;

public class RoleAccessForm extends ValidatorForm {

	private static final long serialVersionUID = 1L;
	private Long id;
	private String parentRead;
	private String parentWrite;
	private String subRead;
	private String subWrite;
	private String parentStr;
	private String systemId;

	public String getSystemId() {
		return systemId;
	}

	public void setSystemId(String systemId) {
		this.systemId = systemId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getParentRead() {
		return parentRead;
	}

	public void setParentRead(String parentRead) {
		this.parentRead = parentRead;
	}

	public String getParentWrite() {
		return parentWrite;
	}

	public void setParentWrite(String parentWrite) {
		this.parentWrite = parentWrite;
	}

	public String getSubRead() {
		return subRead;
	}

	public void setSubRead(String subRead) {
		this.subRead = subRead;
	}

	public String getSubWrite() {
		return subWrite;
	}

	public void setSubWrite(String subWrite) {
		this.subWrite = subWrite;
	}

	public void setParentStr(String parentStr) {
		this.parentStr = parentStr;
	}

	public String getParentStr() {
		return parentStr;
	}

}
